public class BitwiseOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        System.out.println("a&b = "+(a & b));
        System.out.println("a|b = " + (a | b));
        System.out.println("a ^ b = " + (a ^ b));
        System.out.println("~a = " + (~a));
        System.out.println("a>>b = " + (a >> b));
        System.out.println("a<<b = " + (a << b));

    }
}

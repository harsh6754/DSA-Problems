public class ComparisonOperators {
    public static void main(String[] args) {
        int Value1 = 1;
        int Value2 = 2;

        if(Value1 == Value2)
        {
            System.out.println("Value1 == Value2");
        }

        if(Value1 != Value2)
        {
            System.out.println("Value1 != Value2");
        }

        if(Value1 < Value2)
        {
            System.out.println("Value1 < Value2");
        }

        if (Value1 > Value2) {
            System.out.println("Value1 > Value2");
        }

        if (Value1 <= Value2) {
            System.out.println("Value1 <= Value2");
        }
        if (Value1 >= Value2) {
            System.out.println("Value1 >= Value2");
        }
    }
}

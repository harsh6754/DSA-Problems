public class UnaryOperators {
    public static void main(String[] args) {
        int Result = +1;
        System.out.println(Result);

        Result --;
        System.out.println(Result);

        Result ++;
        System.out.println(Result);

        Result = -Result;
        System.out.println(Result);

        boolean Success = true;
        System.out.println(Success);

        System.out.println(!Success);
    }
}

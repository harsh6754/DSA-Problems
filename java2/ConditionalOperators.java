public class ConditionalOperators {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;

        if((value1 ==1) && (value2 == 2))
        {
            System.out.println("Value1 Is 1 & Value2 Is 2");
        }

        if((value1 ==1) || (value2 == 2))
        {
            System.out.println("Value1 Is 1 OR Value2 Is 2");
        }
    }
}

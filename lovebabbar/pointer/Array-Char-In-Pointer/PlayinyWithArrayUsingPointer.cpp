#include<bits/stdc++.h>
using namespace std;

int main()
{
    int arr[100] = {23,122,41,67};
    cout<<arr<<endl;
    cout<<arr[0]<<endl;
    cout<<&arr[0]<<endl;

    cout<<*arr<<endl;
    cout<<*arr +1<<endl;
    cout<<*(arr+1)<<endl;
    cout<<(*arr) + 1<<endl;
    cout<<arr[2]<<endl;
    cout<<*(arr+2)<<endl;
    cout<<*(2+arr)<<endl;

    cout<<endl;
}